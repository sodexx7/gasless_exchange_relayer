async function handler(event) {
  // Parse webhook payload
  if (!event.request || !event.request.body) throw new Error(`Missing payload`);
  const { relayerType, request } = event.request.body;
  console.log(`Relaying`, `relayerType`, relayerType);
  console.log(`request`, request);

  // Initialize Relayer provider and signer, and forwarder contract
  const credentials = { ...event };
  const provider = new DefenderRelayProvider(credentials);
  const signer = new DefenderRelaySigner(credentials, provider, {
    speed: "fast",
  });

  if (relayerType == "permitTokenA") {
    const erc20TokenA = new ethers.Contract(
      erc20TokenAAddress,
      ERC20TokenAAbi,
      signer
    );

    const tx = await relaypPermitToken(
      erc20TokenA,
      request.request,
      request.signature
    );
    console.log(`Sent meta-tx(permitToken): ${tx.hash}`);
    return { txHash: tx.hash };
  } else if (relayerType == "permitTokenB") {
    const erc20TokenB = new ethers.Contract(
      erc20TokenBAddress,
      ERC20TokenBAbi,
      signer
    );
    const tx = await relaypPermitToken(
      erc20TokenB,
      request.request,
      request.signature
    );
    console.log(`Sent meta-tx(permitToken): ${tx.hash}`);
    return { txHash: tx.hash };
  } else if (relayerType == "pairOrdersExecute") {
    const executeOrder = new ethers.Contract(
      executeOrderAddress,
      ExecuteOrderAbi,
      signer
    );

    const tx = await relayPairOrdersExecute(executeOrder, request);

    console.log(`Sent meta-tx(execute Orders): ${tx.hash}`);
    return { txHash: tx.hash };
  }
}

