function splitSignature(signature) {
  let r = "0x" + signature.slice(2, 66);
  let s = "0x" + signature.slice(66, 130);
  let v = parseInt(signature.slice(130, 132), 16);
  return { r, s, v };
}

async function relaypPermitToken(erc20TokenA, request, signature) {
  // // Decide if we want to relay this request based on a whitelist
  // const accepts = !whitelist || whitelist.includes(request.spender);
  // if (!accepts) throw new Error(`Rejected request to ${request.spender}`);

  // Validate request on the forwarder contract
  // const valid = await forwarder.verify(request, signature);
  // if (!valid) throw new Error(`Invalid request`);

  // Send meta-tx through relayer to the forwarder contract
  const gasLimit = (1e6 + 50000).toString();
  const { r, s, v } = splitSignature(signature);
  return await erc20TokenA.permit(
    request.owner,
    request.spender,
    request.value,
    request.deadline,
    v,
    r,
    s,
    { gasLimit }
  );
}

